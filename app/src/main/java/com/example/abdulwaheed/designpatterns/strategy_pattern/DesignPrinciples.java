package com.example.abdulwaheed.designpatterns.strategy_pattern;

/*
 * this class is intended to write all design principles used in this specific design patters.
 * It will be holding all rules of this design pattern in a same and single place.
 * */
public class DesignPrinciples {
    /*
     * Design Pattern Rules
     * ====================
     * 1) Take what varies and "encapsulate" it so it won't affect the rest of your code.
     * 2) Program to an interface, not an implementation
     * 3) Favour composition over inheritance
     *
     * Definition
     * ==========
     * The Strategy Pattern defines a family of algorithms, encapsulate each one, and makes them
     * interchangeable. Strategy lets the algorithm vary independently from clients that use it/
     * */
}
