package com.example.abdulwaheed.designpatterns.template_method_pattern;

/*
 * this class is intended to write all design principles used in this specific design patters.
 * It will be holding all rules of this design pattern in a same and single place.
 * */
public class DesignPrinciples {
    /*
     * Design Pattern Rules
     * ====================
     *
     * Don't call us, we will call you
     *
     * Definition
     * ==========
     * The Template Method defines the steps of an algorithm and allows subclasses to provide the
     * implementation for one or more steps
     * Officially
     * ==========
     * The Template Method Patterns define the skeleton of an algorithm in a method, deferring some
     * steps to subclassess. Template Method lets subclasses redefine certain steps of an algorithm
     * without changing the algorithm structure
     *
     * */
}
