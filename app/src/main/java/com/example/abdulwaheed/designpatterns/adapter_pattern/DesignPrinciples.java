package com.example.abdulwaheed.designpatterns.adapter_pattern;

/*
 * this class is intended to write all design principles used in this specific design patters.
 * It will be holding all rules of this design pattern in a same and single place.
 * */
public class DesignPrinciples {
    /*
     * Design Pattern Rules
     * ====================
     *
     * Definition
     * ==========
     * The Adapter Pattern converts the interface of a class into another interface the clients expect.
     * Adapter lets classes work together that couldn't otherwise because of incompatible interface.
     *
     * Who does What?
     * ==============
     * Decorator ==> Converts one interface to another
     * Adapter ==> Does not alter the interface, but adds responsibility
     * Facade ==> Makes an interface simpler
     * */
}
